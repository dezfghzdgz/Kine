type DictKey = keyof typeof dictionary.cs;

const LanguageContext = createContext<{
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: DictKey) => string;
}>({
  lang: 'cs',
  setLang: () => {},
  t: (key) => dictionary.cs[key],
});

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>('en');

  useEffect(() => {
    const saved = localStorage.getItem('kine-lang') as Lang | null;
    if (saved && ['cs', 'en', 'de', 'sk', 'es', 'pl', 'fr', 'uk'].includes(saved)) setLangState(saved);
  }, []);

  function setLang(l: Lang) {
    setLangState(l);
    localStorage.setItem('kine-lang', l);
  }

  function t(key: DictKey) {
    return dictionary[lang][key] ?? dictionary.cs[key];
  }

  return (
    <LanguageContext.Provider value={{ lang, setLang, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  return useContext(LanguageContext);
}
